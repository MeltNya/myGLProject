void GLObject::CreatePlane()
{
    float planeVertices[] = {
        // positions          // texture Coords (note we set these higher than 1 (together with GL_REPEAT as texture wrapping mode). this will cause the floor texture to repeat)
         5.0f, -0.5f,  5.0f,  2.0f, 0.0f,
        -5.0f, -0.5f,  5.0f,  0.0f, 0.0f,
        -5.0f, -0.5f, -5.0f,  0.0f, 2.0f,

         5.0f, -0.5f,  5.0f,  2.0f, 0.0f,
        -5.0f, -0.5f, -5.0f,  0.0f, 2.0f,
         5.0f, -0.5f, -5.0f,  2.0f, 2.0f
    };
    if (Vertices != nullptr) {
        free(Vertices);
    }
    Vertices = (float*)malloc(5*6* sizeof(float));
    memcpy(Vertices, planeVertices, sizeof(planeVertices));
    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);
    glBindVertexArray(VAO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    // glBufferData(GL_ARRAY_BUFFER, sizeof(cubeVertices), &cubeVertices, GL_STATIC_DRAW);
    glBufferData(GL_ARRAY_BUFFER, 30 * sizeof(float), Vertices, GL_STATIC_DRAW);
    //输入着色器
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float)));
    glBindVertexArray(0);
    num = 6;
}

void GLObject::Draw(Shader& shader)
{
    glBindVertexArray(VAO);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, texture);
    shader.setMat4("model", model); 
    glm::mat4 projection = glm::perspective(glm::radians(45.0f), 800.0f / 600.0f, 0.1f, 100.0f);
    shader.setMat4("projection", projection);
    glDrawArrays(GL_TRIANGLES, 0, num);
}

void GLObject::Clear()
{
     glDeleteVertexArrays(1, &VAO);
     glDeleteBuffers(1, &VBO);
     glDeleteBuffers(1, &EBO);
}



void GLObject::SetTexture(unsigned int& _texture, Shader& shader)
{
    texture = _texture;
    shader.Use();
    shader.setInt("texture1", 0);
}

void GLObject::SetPosition(glm::vec3 position)
{
    model = glm::translate(model, position);
}
